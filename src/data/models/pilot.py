class Pilot:
    def __init__(self, name, age, email, phone_num):
        self.name = name
        self.age = age
        self.email = email
        self.phone_num = phone_num

    def get_name(self):
        return self.name

    def get_age(self):
        return self.age

    def get_email(self):
        return self.email

    def get_phone_num(self):
        return self.phone_num
